def process_flags_in_file(input_file, acceptable_flags):
    # Regex to find FLAG_ followed by alphanumeric characters (until space or newline)
    flag_pattern = re.compile(r'\bTRAINER_\w+')

    updated_lines = []

    # Open the input file for reading
    with open(input_file, 'r') as infile:
        for line in infile:
            # Search for all occurrences of FLAG_ in the line
            flags_in_line = flag_pattern.findall(line)

            # If the line contains FLAG_, process it
            for flag in flags_in_line:
                # Check if the flag is in the acceptable flags list
                if flag not in acceptable_flags:
                    # Replace the invalid flag with FLAG_GARBAGEFLAG
                    line = line.replace(flag, 'FLAG_GARBAGEFLAG')

            # Add the processed line to the updated lines list
            updated_lines.append(line)

    # Rewrite the file with the updated lines
    with open(input_file, 'w') as outfile:
        outfile.writelines(updated_lines)

def process_directory(root_dir, acceptable_flags):
    # Walk through the directory and its subdirectories
    for dirpath, _, filenames in os.walk(root_dir):
        for filename in filenames:
            # Only process the scripts.inc file
            if filename == 'scripts.inc':
                input_file = os.path.join(dirpath, filename)
                print("Processing file: {}".format(input_file))

                # Call process_flags_in_file for each scripts.inc file
                process_flags_in_file(input_file, acceptable_flags)

